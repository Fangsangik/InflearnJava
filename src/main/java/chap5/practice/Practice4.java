package chap5.practice;

public class Practice4 {
    public static void main(String[] args) {
        for (int i = 1; i < 10; i++) {
            for (int j = 1; j < 10; j++) {
                System.out.print(i + " * " + j + " = " +  j * i);
                System.out.println();
            }
            System.out.println();
        }

    }
}
