package chap2;

public class Var7 {
    public static void main(String[] args) {
        int a = 100;
        double b = 10.5;
        boolean c = true;
        char d = 'A';
        String e = "Hello";
        System.out.println("e = " + e);
        System.out.println("d = " + d);
        System.out.println("c = " + c);
        System.out.println("b = " + b);
        System.out.println("a = " + a);
    }
}
